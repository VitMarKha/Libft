/*
** ft_isprint: возвращает ненулевое значение
** если c это печатаемый символ (0x20 - 0x7E).
*/

int	ft_isprint(int c)
{
	return (((c >= 040) && (c <= 047)) ||
			(((c >= 050) && (c <= 057)) ||
			 (((c >= 060) && (c <= 067)) ||
			  (((c >= 070) && (c <= 077)) ||
			   (((c >= 100) && (c <= 107)) ||
				(((c >= 110) && (c <= 117)) ||
				 (((c >= 120) && (c <= 127)) ||
				  (((c >= 130) && (c <= 137)) ||
				   (((c >= 140) && (c <= 147)) ||
					(((c >= 150) && (c <= 157)) ||
					 (((c >= 160) && (c <= 167)) ||
					  (((c >= 170) && (c <= 176)) ? 1 : 0))))))))))));
}
